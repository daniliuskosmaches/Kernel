// ============================================================
// СОСТОЯНИЕ МОДИФИКАТОРОВ
// ============================================================
static uint8_t shift_pressed = 0;
static uint8_t ctrl_pressed = 0;
static uint8_t alt_pressed = 0;
static uint8_t caps_lock = 0;

// ============================================================
// ВНЕШНЯЯ ФУНКЦИЯ ДЛЯ ПЕРЕДАЧИ СИМВОЛОВ В SHELL
// ============================================================
extern void shell_handle_char(char c);

// ============================================================
// ИНИЦИАЛИЗАЦИЯ КЛАВИАТУРЫ
// ============================================================
void init_keyboard(void) {
    // Регистрируем обработчик для IRQ1 (вектор 0x21)
    register_interrupt_handler(IRQ1, keyboard_handler_c);

    // Включаем IRQ1 в контроллере прерываний
    pic_enable_irq(1);
}

// ============================================================
// ГЛАВНЫЙ ОБРАБОТЧИК ПРЕРЫВАНИЯ КЛАВИАТУРЫ
// ============================================================
void keyboard_handler_c(registers_t *regs) {
    (void)regs;  // Неиспользуемый параметр

    // Читаем скан-код с порта клавиатуры
    uint8_t scancode = inb(KEYBOARD_DATA_PORT);

    // ========================================================
    // ОБРАБОТКА ОТПУСКАНИЯ КЛАВИШ (Release)
    // ========================================================
    // Старший бит (0x80) = 1 означает отпускание клавиши
    if (scancode & 0x80) {
        scancode &= 0x7F;  // Убираем бит Release

        // Обрабатываем модификаторы
        if (scancode == 0x2A || scancode == 0x36) {
            shift_pressed = 0;  // Left Shift или Right Shift
        } else if (scancode == 0x1D) {
            ctrl_pressed = 0;   // Ctrl
        } else if (scancode == 0x38) {
            alt_pressed = 0;    // Alt
        }

        return;  // Не обрабатываем отпускание других клавиш
    }

    // ========================================================
    // ОБРАБОТКА НАЖАТИЯ МОДИФИКАТОРОВ
    // ========================================================
    if (scancode == 0x2A || scancode == 0x36) {
        shift_pressed = 1;  // Left Shift (0x2A) или Right Shift (0x36)
        return;
    }

    if (scancode == 0x1D) {
        ctrl_pressed = 1;   // Ctrl
        return;
    }

    if (scancode == 0x38) {
        alt_pressed = 1;    // Alt
        return;
    }

    if (scancode == 0x3A) {
        caps_lock = !caps_lock;  // Caps Lock (toggle)
        return;
    }

    // ========================================================
    // ПРЕОБРАЗОВАНИЕ СКАН-КОДА В СИМВОЛ
    // ========================================================
    char c = 0;

    if (scancode < 128) {
        // Выбираем раскладку в зависимости от Shift
        if (shift_pressed) {
            c = kbd_us_shift[scancode];
        } else {
            c = kbd_us[scancode];
        }

        // Применяем Caps Lock для букв
        if (caps_lock) {
            if (c >= 'a' && c <= 'z') {
                c = c - 'a' + 'A';  // Нижний регистр → верхний
            } else if (c >= 'A' && c <= 'Z') {
                c = c - 'A' + 'a';  // Верхний регистр → нижний
            }
        }
    }

    // ========================================================
    // ОБРАБОТКА СИМВОЛА
    // ========================================================
    if (c != 0) {
        // ====================================================
        // СПЕЦИАЛЬНЫЕ КОМБИНАЦИИ КЛАВИШ
        // ====================================================

        // Ctrl+C: Прерывание (пока просто выводим символ)
        if (ctrl_pressed && c == 'c') {
            terminal_write_string("^C\n");
            return;
        }

        // Ctrl+L: Очистка экрана
        if (ctrl_pressed && c == 'l') {
            terminal_clear_screen();
            return;
        }

        // Ctrl+D: Exit (можно добавить обработку позже)
        if (ctrl_pressed && c == 'd') {
            terminal_write_string("^D\n");
            return;
        }

        // ====================================================
        // ПЕРЕДАЧА СИМВОЛА В SHELL
        // ====================================================
        shell_handle_char(c);
    }

    // EOI (End of Interrupt) отправляется автоматически в isr_handler_c
    // Поэтому здесь ничего делать не нужно
}

// ============================================================
// АЛЬТЕРНАТИВНАЯ ФУНКЦИЯ УСТАНОВКИ (для обратной совместимости)
// ============================================================
void keyboard_install(void) {
    init_keyboard();
}